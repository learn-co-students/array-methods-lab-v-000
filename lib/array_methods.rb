def using_include(array, element)
  # Returns either, true or false if the *element exists in the *array.
  array.include?(element)
end

def using_sort(array)
  # Returns a sorted order of array
  array.sort
end

def using_reverse(array)
  # Returns a reverse order of the array.
  array.reverse
end

def using_first(array)
  # Returns the first element in array
  array.first
end

def using_last(array)
  # Returns last element in array
  array.last
end

def using_size(array)
  # Returns the number of elements within the array.
  array.size
end
