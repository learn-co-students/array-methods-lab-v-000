#looks to see if element is part of the array

def using_include(array, element)
  array.include?(element)
end

#sorts the element in order

def using_sort(array)
  array.sort
end

#displays the array in reverse order

def using_reverse(array)
  array.reverse
end

#retrieves the first element in the array

def using_first(array)
  array.first
end

#retrieves the last element in the array

def using_last(array)
  array.last
end

#gets get the size (number of elements in the array)

def using_size(array)
  array.size
end
