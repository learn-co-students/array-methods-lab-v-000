def using_include(array, element)
 array.include?(element)
 #checks if element is in array
end

def using_sort(array)
 array.sort
 #sorts array. Strings alphabetically, Numbers by value.
end

def using_reverse(array)
 array.reverse
 #flips order of array
end

def using_first(array)
 array.first
 #pulls first element of array
end

def using_last(array)
 array.last
 #pulls last element of array
end

def using_size(array)
 array.size
 #counts total ammount of elements in an array.
end
